    private void jBtCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtCancelarActionPerformed

        int t = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente cancelar o cadastro?");
        if (t == 0) {
            situacao = 2;
            lcampos.LimparCampos(jPCadastro);
            estadobotoes(false);
        }
    }//GEN-LAST:event_jBtCancelarActionPerformed

    private void jBtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGravarActionPerformed
        char estoque;
        char financeiro;
        char operacao;
        if(jCBEstoque.isSelected()){
             estoque = 'S';
        }else{
             estoque = 'N';
        }
        if(jCBFinanceiro.isSelected()){
             financeiro = 'S';
        }else{
             financeiro = 'N';
        }
        if(jTFDsMvto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "o Campo descrição deve ser preenchido");
        }else{
            tipoMovto.setDSMVTO(jTFDsMvto.getText().toUpperCase());
        }
        if (jCBTpOperacao.getSelectedIndex() == 0){
            operacao = 'E';
        }else{
            operacao = 'S';
        }
        if(situacao == 1) {
             
           tipoMovto.setMOVESTOQUE(String.valueOf(estoque));
           tipoMovto.setMOVFINANCEIRO(String.valueOf(financeiro));
           tipoMovto.setTPMOVTO(String.valueOf(operacao));
           
           tpMovtoDAO.incluir(tipoMovto);
           lcampos.LimparCampos(jPCadastro);
           estadobotoes(false);
        }else{
           tipoMovto.setMOVESTOQUE(String.valueOf(estoque));
           tipoMovto.setMOVFINANCEIRO(String.valueOf(financeiro));
           tipoMovto.setTPMOVTO(String.valueOf(operacao));
           
           tpMovtoDAO.alterar(tipoMovto);
           lcampos.LimparCampos(jPCadastro);
           estadobotoes(false);
        }
    }//GEN-LAST:event_jBtGravarActionPerformed

    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed

         PreencherJtableGenerico preencher = new PreencherJtableGenerico();
        switch (jCbPesquisa2.getSelectedIndex()) {
            case 0: {
                tpMovtoDAO.consulta(tipoMovto);
                preencher.PreencherJtableGenerico(jTbPesquisa, tipoMovto.getRetorno());
                return;
            }
    }                       
    }//GEN-LAST:event_jBPesquisarActionPerformed

    private void jTbPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTbPesquisaMouseClicked

         if (evt.getClickCount() == 1) {
            int linha = jTbPesquisa.getSelectedRow();
            String ID = (String) jTbPesquisa.getValueAt(linha, 0);

            tipoMovto.setIDTPMOVTO(Integer.parseInt(ID));
            tpMovtoDAO.retornadados(tipoMovto); 
            String est = tipoMovto.getMOVESTOQUE();
            String fin = tipoMovto.getMOVFINANCEIRO();
            String tipo = tipoMovto.getTPMOVTO();
            
            jTFDsMvto.setText(tipoMovto.getDSMVTO());
            
            jTFTipoMvto.setText(Integer.toString(tipoMovto.getIDTPMOVTO()));
            if(est.equals("S")){
                jCBEstoque.setSelected(true);
            }
            if(fin.equals("S")){
                jCBFinanceiro.setSelected(true);
            }
            if(tipoMovto.getTPMOVTO().equals("E")){
                jCBTpOperacao.setSelectedItem("Entada");
            }else{
                jCBTpOperacao.setSelectedItem("Saída");
            }
            
        }
        estadobotoes(false);
        jTPNivel.setSelectedIndex(0);
    }//GEN-LAST:event_jTbPesquisaMouseClicked

