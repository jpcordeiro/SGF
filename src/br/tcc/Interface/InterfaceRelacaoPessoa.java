    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed
        if (!jTFIdPessoa.getText().equals("")) {
            if (!jTFNomerelacao.getText().equals("")) {
                if (!jTFTpAmizade.getText().equals("")) {
                    IncluirLista();
                } else {
                    JOptionPane.showMessageDialog(null, "Informe o tipo de amizade!");
                    jTFNomerelacao.grabFocus();
                }
            }else {
                    JOptionPane.showMessageDialog(null, "Informe o nome!");
                    jTFNomerelacao.grabFocus();
                }
        }else {
                    JOptionPane.showMessageDialog(null, "Informe o codigo!");
                    jTFNomerelacao.grabFocus();
                }
    }//GEN-LAST:event_jBAddActionPerformed

