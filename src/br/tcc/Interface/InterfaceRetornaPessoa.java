    private void jBGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarActionPerformed
              
        if(situacao == 1){
            pessoa.setDSPESSOA(jTFDsPessoa.getText().toUpperCase());
            pessoa.setDTNASC(jTFDtNasc.getText());
            
            if(jTFDsPessoa.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Informe o nome da Pessoa");
                jTFDsPessoa.grabFocus();
            }
            if(jTFDtNasc.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Informe a Data de Nascimento");
                jTFDtNasc.grabFocus();
            } else {
            }
            pessoaDAO.incluir(pessoa);
             JOptionPane.showMessageDialog(null, "Pessoa Cadastrada com sucesso!");
             lCampos.LimparCampos(jPCadastro);
             this.setVisible(true);
        }else{
             if(situacao == 2){
            pessoa.setDSPESSOA(jTFDsPessoa.getText().toUpperCase());
            pessoa.setDTNASC(jTFDtNasc.getText());
            
            if(jTFDsPessoa.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Informe o nome da Pessoa");
                jTFDsPessoa.grabFocus();
            }
            if(jTFDtNasc.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Informe a Data de Nascimento");
                jTFDtNasc.grabFocus();
            }
            pessoaDAO.alterar(pessoa);
             JOptionPane.showMessageDialog(null, "Pessoa Cadastrada com sucesso!");
             lCampos.LimparCampos(jPCadastro);
             }
        }
       
    }//GEN-LAST:event_jBGravarActionPerformed

