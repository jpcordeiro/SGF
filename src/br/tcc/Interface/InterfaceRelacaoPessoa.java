 private void estadobotoes(boolean situacao) {
        jBAdicionar.setEnabled(!situacao);
        jBAlterar.setEnabled(!situacao);
        jBExcluir.setEnabled(!situacao);
        jBGravar.setEnabled(situacao);
        jBCancelar.setEnabled(situacao);
    }
 
 public void IncluirLista() {
        DefaultTableModel ItensFornece = (DefaultTableModel) jTrelacao.getModel();

        int totlinha = jTrelacao.getRowCount();
        int incluir = 0;

        if (incluir == 0) {
            ItensFornece.setNumRows(totlinha + 1);
            ItensFornece.setValueAt(jTFIdPessoa.getText(), totlinha, 0);
            ItensFornece.setValueAt(jTFNomerelacao.getText(), totlinha, 1);
            ItensFornece.setValueAt(jCBcomemoracao.getSelectedItem(), totlinha, 2);
            ItensFornece.setValueAt(jTFData.getText(), totlinha, 3);
            ItensFornece.setValueAt(jTFTpAmizade.getText(), totlinha, 4);

            jTFNomerelacao.setText("");
            jCBcomemoracao.setSelectedIndex(0);
            jTFData.setText("");
            jTFTpAmizade.setText("");
        }
    }
