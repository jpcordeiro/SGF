    private void jBPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisaActionPerformed
        PreencherJtableGenerico preencher = new PreencherJtableGenerico();
       
        switch(jCBPesquisa.getSelectedIndex()){
            case 0:{
                pessoaDAO.consulta(pessoa);
                preencher.PreencherJtableGenerico(jTConsulta, pessoa.getRetorno());
                return;
            }
            case 1:{
                if (!jTFPesquisa.getText().equals("")){
                    pessoa.setDSPESSOA(jTFPesquisa.getText().toUpperCase());
                    pessoaDAO.consultadescricao(pessoa);
                    preencher.PreencherJtableGenerico(jTConsulta, pessoa.getRetorno());
                return;
                    }
            }
        }
    }//GEN-LAST:event_jBPesquisaActionPerformed

